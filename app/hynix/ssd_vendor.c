/*********************************************************************************************************
** Function Name         : DelayMS  
** Function Describe     :
** Input  Parameter      : None
** Output Parameter      : None
** Global Parameter      : None
** Call Module           :
**------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void DelayMS(INT32U dly)
{
  INT32U i;

  for(; dly > 0; dly--)
  {
    for(i =0; i <40000; i++)
    {
      ;
    }
  }
}

/*********************************************************************************************************
** Function Name         : DelayUS  
** Function Describe     :
** Input  Parameter      : None
** Output Parameter      : None
** Global Parameter      : None
** Call Module           :
**------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void DelayUS(INT32U dly) //  1US
{
  INT32U i;

  for(; dly > 0; dly--)
  {
    for(i = 0; i < 40; i++)
    {
      ;
    }
  }
}

